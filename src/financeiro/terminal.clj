;; ==============================
;;     EXTRATO POR PER√çODO
;; ==============================
(defn mostrar-extrato-por-periodo []
  (print "Data inicial (YYYY-MM-DD): ") (flush)
  (let [di (read-line)]
    (print "Data final (YYYY-MM-DD): ") (flush)
    (let [df (read-line)]
      (try
        (let [res (db/filtrar-por-periodo di df)]
          (println "\n==== EXTRATO POR PER√çODO ====")
          (if (empty? res)
            (println "Nenhuma transa√ß√£o encontrada nesse per√≠odo.")
            (doseq [t res]
              (println t)))
          (println "==============================\n"))
        (catch Exception e
          (println "Erro ao filtrar per√≠odo:" (.getMessage e)))))))

;; ==============================
;;     SALDO DA CARTEIRA
